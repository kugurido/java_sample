package bean;

public class Item {

	int i_id;
	String i_name;
	String i_img;
	String i_genre;
	String i_publisher;
	String i_author;


	public int getI_id() {
		return i_id;
	}
	public void setI_id(int i_id) {
		this.i_id = i_id;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public String getI_img() {
		return i_img;
	}
	public void setI_img(String i_img) {
		this.i_img = i_img;
	}
	public String getI_genre() {
		return i_genre;
	}
	public void setI_genre(String i_genre) {
		this.i_genre = i_genre;
	}
	public String getI_publisher() {
		return i_publisher;
	}
	public void setI_publisher(String i_publisher) {
		this.i_publisher = i_publisher;
	}
	public String getI_author() {
		return i_author;
	}
	public void setI_author(String i_author) {
		this.i_author = i_author;
	}



}
