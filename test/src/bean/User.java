package bean;

public class User {

	int u_id;
	String u_name;
	String u_kana;
	String u_mail;
	String u_img;
	String u_login;
	String u_pass;
	String u_pr;




	public int getU_id() {
		return u_id;
	}
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public String getU_kana() {
		return u_kana;
	}
	public void setU_kana(String u_kana) {
		this.u_kana = u_kana;
	}
	public String getU_mail() {
		return u_mail;
	}
	public void setU_mail(String u_mail) {
		this.u_mail = u_mail;
	}

	public String getU_img() {
		return u_img;
	}
	public void setU_img(String u_img) {
		this.u_img = u_img;
	}
	public String getU_login() {
		return u_login;
	}
	public void setU_login(String u_login) {
		this.u_login = u_login;
	}
	public String getU_pass() {
		return u_pass;
	}
	public void setU_pass(String u_pass) {
		this.u_pass = u_pass;
	}
	public String getU_pr() {
		return u_pr;
	}
	public void setU_pr(String u_pr) {
		this.u_pr = u_pr;
	}

}
