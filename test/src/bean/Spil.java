package bean;

public class Spil {

	int spil_id;
	int user_id;
	String spil_time;
	String spil;
	String user_img;

	public int getSpil_id() {
		return spil_id;
	}
	public void setSpil_id(int spil_id) {
		this.spil_id = spil_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getSpil_time() {
		return spil_time;
	}
	public void setSpil_time(String spil_time) {
		this.spil_time = spil_time;
	}
	public String getSpil() {
		return spil;
	}
	public void setSpil(String spil) {
		this.spil = spil;
	}
	public String getUser_img() {
		return user_img;
	}
	public void setUser_img(String user_img) {
		this.user_img = user_img;
	}


}
